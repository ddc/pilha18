#include "pilha.h"

/////////////////////////////////////////////////////////////////////////////
///////////////////////////////////// push////////////////////////////////
/////////////////////////////////////////////////////////////////////////////
void Pilha::push(int i, int el, long* pilhaVet)
{
	pilhaVet[i] = el;
	qtdePush++;
	qtdeElementos++;
}
/////////////////////////////////////////////////////////////////////////////
///////////////////////////////////// pop   ////////////////////////////////
/////////////////////////////////////////////////////////////////////////////
void Pilha::pop(int i, long* pilhaVet)
{
	pilhaVet[i] = 0;
	qtdePop++;
	qtdeElementos--;
}
/////////////////////////////////////////////////////////////////////////////
///////////////////////////////////// VERIFICAR VAZIA ///////////////////////
/////////////////////////////////////////////////////////////////////////////
bool Pilha::vazia()
{
  if(qtdeElementos <= 0)
	  return true;

  return false;
}
/////////////////////////////////////////////////////////////////////////////
///////////////////////////////////// VERIFICAR CHEIA ///////////////////////
/////////////////////////////////////////////////////////////////////////////
bool Pilha::cheia()
{
  if(qtdeElementos >= tamanho)
	  return true;

  return false;
}
/////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////// CONSTRUTOR ////////////////////
/////////////////////////////////////////////////////////////////////////////
Pilha::Pilha(long tam)
{
	//pilha = (long*) malloc(tam* sizeof(long));
	//pilha = new long(tam);
	tamanho 		= tam;
	qtdePush 		= 0;
	qtdePop 		= 0;
	qtdeElementos 	= 0;
	qtdeRedMaior	= 0;
	qtdeRedMenor	= 0;
}
